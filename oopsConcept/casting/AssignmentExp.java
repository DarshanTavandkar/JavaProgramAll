package casting;

public class AssignmentExp {

	public static void main (String args[])
	{
		int a = 60;
		
		System.out.println(a);
		
		double b = (double)a;
		
		System.out.println(b);
		
		float c = (float)b;
		
		System.out.println(c);
		
		short d =(short)c;
		System.out.println(d);
		
		byte e = (byte)d;
		System.out.println(e);	
		
	}
	
		
}
