package stringexamples;

public class StringMethod2 {

	public static void main(String[] args) {
		
		String a= "Faith is the Bird that FEELS the light and sings when the dawn is still dark";

		String sl = a.toLowerCase();
		
		System.out.println(sl);
		
		String su = a.toUpperCase();
		
		System.out.println(su);
	}

}
