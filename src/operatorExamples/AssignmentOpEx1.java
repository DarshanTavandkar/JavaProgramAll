package operatorExamples;

public class AssignmentOpEx1 {
	
	public static void main(String args[])
	
	{
		int a=10;
		System.out.println(a);
		a +=15;
		System.out.println(a);
		a *= 20;
		System.out.println(a);
				
	//-----------------------------------------
		int b=20;
		
		b <<= 5;	
		System.out.println(b);
		b >>= 6;
		System.out.println(b);
		
	//-------------------------------------------	
		int c= 20;
	
		c|=30;
	
		System.out.println(c);
			
			
	}
}
