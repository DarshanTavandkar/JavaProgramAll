package operatorExamples;

public class ArithmeticOpEx {
	
	public static void main(String args[])
	{
		int a=30;
		int b=20;
		int c=10;
		
	   System.out.println(a+b+c);
	   System.out.println(a-b+c);
	   System.out.println(a*b*c);
	   
	   //div & modules
		
		System.out.println(a/c);
		System.out.println(a%c);
		
		
		double d=80;
		double e=60;
		double f=40;
		
		System.out.println(d+e+f);
		System.out.println(d+e-f);
		System.out.println(d*e*f);
		System.out.println(e/d);
		System.out.println(e%d);
	}
	
		

}
