package operatorExamples;

public class ShiftOpEx1 {
	
	public static void main (String arg[])
	{
		//left Shift
	System.out.println(10<<8);	//10*2^8=10*256=2560
	System.out.println(20<<8);	//20*2^8=20*256=5120
	System.out.println(30<<8);	//30*2^8=30*256=7680
	System.out.println(40<<8);	//40*2^8=40*256=10240
		
		//Right Shift
		
	System.out.println(600>>8);//600/2^8=2
	System.out.println(800>>8);//800/2^8=3
	System.out.println(1000>>8);//1000/2^8=3
	
	}
	
		

}
